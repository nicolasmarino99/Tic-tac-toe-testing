class Game
  def initialize
    print_title
    print 'Player 1 name: '
    player1 = gets.chomp
    print 'Player 2 name: '
    player2 = gets.chomp
    @player1 = Player.new(player1, 'X')
    @player2 = Player.new(player2, 'O')
    @board = Board.new
  end

  def play
    puts @board

    loop do
      movePosition(@player1)
      
      if @board.tie?
        puts "I's a tie."
        return
      end
      movePosition(@player2)
    end
  end

  def movePosition(player)
    position1 = player.get_position( @board.board)
    player.move(position1, @board.board)
    puts @board
    if @board.winner?(player.mark)
      puts Paint["The winner is '#{player.name}'!", :green, :bright]
      return
    end
  end

